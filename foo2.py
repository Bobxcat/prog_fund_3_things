def from_radix(raw: str, radix: int):
    s = 0
    place = 1
    for digit_idx in range(0, len(raw)):
        digit_char = raw[len(raw)-digit_idx-1]
        digit = int(digit_char) * place

        s += digit

        place *= radix
    
    return s

def from_digits(digits):
    num = 0
    for i in range(0, len(digits)):
        num += ((2 ** 64) ** i) * digits[i]
    return num

def print_from_digits(digits):
    print(from_digits(digits))

def test_correcteness(dig_raw: int, dig: list, place: list, s: list, s_new: list):
    dig_num = from_digits(dig)
    place_num = from_digits(place)
    s_num = from_digits(s)
    s_new_num = from_digits(s_new)

    mul_worked = dig_raw * place_num == dig_num
    add_worked = s_new_num - s_num == dig_num

    print("=====")
    print(f"dig:        {dig_raw}")
    print(f"place:      {place_num}")
    print(f"dig_scaled: {dig_num}")
    print(f"s:           {s_num}")
    print(f"s new:      {s_new_num}")
    if not mul_worked:
        print("MUL FAIL!!")
    if not add_worked:
        print("ADD FAIL!!")
    print()


